// p3: count whitespace-separated words from stdin
#include <iostream>
#include <string>
using namespace std;
int main(){
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    long long cnt=0; 
    string w;
    while(true){
        cout << "errr";
    }
    while (cin >> w) {
        cnt++;
    }
    
    cout<<cnt<<"\n";
    
    return 0;
}
